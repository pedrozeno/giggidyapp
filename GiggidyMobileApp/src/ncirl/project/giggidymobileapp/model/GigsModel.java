package ncirl.project.giggidymobileapp.model;

public class GigsModel {
	
	private String gigName;
	private String venueName;
	private String gigDate;
	private String artistImg;
	public String getGigName() {
		return gigName;
	}
	public void setGigName(String gigName) {
		this.gigName = gigName;
	}
	public String getVenueName() {
		return venueName;
	}
	public void setVenueName(String venueName) {
		this.venueName = venueName;
	}
	public String getGigDate() {
		return gigDate;
	}
	public void setGigDate(String gigDate) {
		this.gigDate = gigDate;
	}
	public String getArtistImg() {
		return artistImg;
	}
	public void setArtistImg(String artistImg) {
		this.artistImg = artistImg;
	}
	
	
	

}
